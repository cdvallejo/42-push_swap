#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	pos;

	i = 0;
	pos = 0;
	while (s[i] != '\0')
	{
		i++;
		pos++;
	}
	i--;
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
		{
			return ((char *)(s + i));
		}
		i--;
	}
	if (s[pos] == (unsigned char)c)
		return ((char *)(s + pos));
	return (0);
}
